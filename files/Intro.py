# print a blank line
print()

# assign the value of 3 to x
x = 3

# print the value of x
print("x = ", x)


# create a list of numbers
l = [1,6,3,20]

# print the list
print("list contains:", l)
print()


# loop through each element of the list and print out each number
print("The numbers in the list are:")
for num in l :
    print(num)
print()

# print the numbers that are greater than 5
print("The numbers in the list greater than 5 are:")
for num in l :
    if num > 5 :
        print(num)
print()

